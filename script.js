const langCycle = ['it', 'en', 'es'];

const html = document.documentElement;
const langSwitch = document.getElementById('langSwitch');
const langFlag = document.getElementById('langFlag');
const langLabel = document.getElementById('langLabel');
const themeToggle = document.getElementById('themeToggle');
const themeIcon = document.getElementById('themeIcon');

function setTheme(theme) {
  html.setAttribute('data-theme', theme);
  themeIcon.textContent = theme === 'dark' ? '☀️' : '🌙';
  localStorage.setItem('laura-theme', theme);
}

function setLang(lang) {
  html.setAttribute('data-lang', lang);
  const t = translations[lang];
  langFlag.textContent = t.flag;
  langLabel.textContent = t.lang;
  localStorage.setItem('laura-lang', lang);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.innerHTML = t[key];
      }
    }
  });

  const footer = document.querySelector('footer p');
  if (footer) footer.textContent = t.footer;
}

function cycleLang() {
  const current = html.getAttribute('data-lang') || 'it';
  const idx = langCycle.indexOf(current);
  const next = langCycle[(idx + 1) % langCycle.length];
  setLang(next);
}

// Theme
function applySystemTheme() {
  if (localStorage.getItem('laura-theme')) return;
  const prefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;
  setTheme(prefersDark ? 'dark' : 'light');
}

window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', (e) => {
  if (!localStorage.getItem('laura-theme')) {
    setTheme(e.matches ? 'dark' : 'light');
  }
});

themeToggle.addEventListener('click', () => {
  const current = html.getAttribute('data-theme');
  setTheme(current === 'dark' ? 'light' : 'dark');
});

// Language
langSwitch.addEventListener('click', cycleLang);

// Init
if (localStorage.getItem('laura-theme')) {
  setTheme(localStorage.getItem('laura-theme'));
} else {
  applySystemTheme();
}

if (localStorage.getItem('laura-lang')) {
  setLang(localStorage.getItem('laura-lang'));
} else {
  setLang('it');
}